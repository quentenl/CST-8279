# getting user input for miles driven and gallons used
miles_driven = int(input("How many miles were driven? "))
gallons_used = int(input("How many gallons were used? "))

# use miles_driven and gallons_used to calculate MPG
miles_per_gallon = miles_driven / gallons_used

#print output to user nicely, of miles per gallon
print("The average miles per gallon used for ",  miles_per_gallon , "miles per gallon" )